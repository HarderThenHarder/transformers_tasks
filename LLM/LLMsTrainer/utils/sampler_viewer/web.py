    try:
        data_dis = eval(data_distribution)
    except Exception as e:
        if ',' in data_distribution:
            sources, nums = data_distribution.split('\n')
            sources = sources.split(',')
            nums = nums.split(',')
            data_dis = {}
            for s, n in zip(sources, nums):
                data_dis[s] = float(n)
        else:
            raise ValueError('Not invalid input.')
    
